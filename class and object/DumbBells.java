class DumbBells{

    String brand;
    double weight;
    String material;
    String color;
    int price;
    boolean isAdjustable;
    String type;
    double gripSize;
    String origin;
    boolean isRubberCoated;

    public DumbBells(String brand, double weight, String material, String color,
                     int price, boolean isAdjustable, String type,
                     double gripSize, String origin, boolean isRubberCoated) {

        this.brand = brand;
        this.weight = weight;
        this.material = material;
        this.color = color;
        this.price = price;
        this.isAdjustable = isAdjustable;
        this.type = type;
        this.gripSize = gripSize;
        this.origin = origin;
        this.isRubberCoated = isRubberCoated;
    }
}
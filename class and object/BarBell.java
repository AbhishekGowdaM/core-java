class BarBell{

    String brand;
    double weight;
    String material;
    String color;
    int price;
    boolean isAdjustable;
    double length;
    double gripSize;
    String origin;
    boolean hasKnurling;

    public BarBell(String brand, double weight, String material, String color,
                   int price, boolean isAdjustable, double length,
                   double gripSize, String origin, boolean hasKnurling) {

        this.brand = brand;
        this.weight = weight;
        this.material = material;
        this.color = color;
        this.price = price;
        this.isAdjustable = isAdjustable;
        this.length = length;
        this.gripSize = gripSize;
        this.origin = origin;
        this.hasKnurling = hasKnurling;
    }
}
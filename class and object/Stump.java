class Stump{

    String material;
    double height;
    double weight;
    String color;
    boolean isWood;
    int price;
    String brand;
    double diameter;
    String origin;
    boolean isPolished;

    public Stump(String material, double height, double weight, String color,
                 boolean isWood, int price, String brand, double diameter,
                 String origin, boolean isPolished){

        this.material = material;
        this.height = height;
        this.weight = weight;
        this.color = color;
        this.isWood = isWood;
        this.price = price;
        this.brand = brand;
        this.diameter = diameter;
        this.origin = origin;
        this.isPolished = isPolished;
    }
}
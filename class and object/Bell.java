class Bell{

    String material;
    double weight;
    String color;
    String type;
    int price;
    String brand;
    double size;
    String soundType;
    String origin;
    boolean isElectric;

    public Bell(String material, double weight, String color, String type,
                int price, String brand, double size, String soundType,
                String origin, boolean isElectric) {

        this.material = material;
        this.weight = weight;
        this.color = color;
        this.type = type;
        this.price = price;
        this.brand = brand;
        this.size = size;
        this.soundType = soundType;
        this.origin = origin;
        this.isElectric = isElectric;
    }
}
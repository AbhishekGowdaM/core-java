class AsianPaint{

    String brand;
    String color;
    String finishType;
    double quantity;
    int price;
    boolean isWaterProof;
    String type;
    String surface;
    String origin;
    boolean isEcoFriendly;

    public AsianPaint(String brand, String color, String finishType, double quantity,
                      int price, boolean isWaterProof, String type,
                      String surface, String origin, boolean isEcoFriendly) {

        this.brand = brand;
        this.color = color;
        this.finishType = finishType;
        this.quantity = quantity;
        this.price = price;
        this.isWaterProof = isWaterProof;
        this.type = type;
        this.surface = surface;
        this.origin = origin;
        this.isEcoFriendly = isEcoFriendly;
    }
}